def dollars2cents(dollars):
	cents = dollars * 100
	return cents	

def gallons2liters(gallons):
	liters = gallons * 3.78541
	return liters

def hours2minutes(hours):
	minutes = hours * 60
	return minutes

def feet2inches(feet):
	inch = feet * 12
	return inches

def degrees2radians(degrees):
	radians = degrees * 0.0174533
	return radians


